package com.huaweijoshua.samples;

public class HTRRequest {
    private String strokes;
    private String ip;
    public String getStrokes() {
        return strokes;
    }

    // Setter
    public void setStrokes(String newStroke) {
        this.strokes = newStroke;
    }

    public String getip() {
        return ip;
    }

    // Setter
    public void setip(String newip) {
        this.ip = newip;
    }
}
